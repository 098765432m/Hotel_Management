import type { Metadata } from "next";
import { Inter } from "next/font/google";
import "./globals.css";
import styles from "@/styles/global.module.scss";
import { ConfigProvider } from "antd";

// Require for Mantine core

import { MantineProvider, ColorSchemeScript } from "@mantine/core";
import "@mantine/core/styles.css"; // mantine core
import "@mantine/carousel/styles.css"; // mantine carousel

// Require for Mantine core

//import Redux

import ReduxProvider from "@/components/redux/ReduxProvider";

//import Redux

import Header from "@/components/header/Header";
import { AppRouterCacheProvider } from "@mui/material-nextjs/v13-appRouter";
import { cookies } from "next/headers";
import { UserCookieResponse } from "@/types/dto/user.dto";
import { decrypt, SessionPayload } from "@/lib/session";
import Footer from "@/components/footer/Footer";
import DashboardHeader from "@/components/header/dashboard/DashboardHeader";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const cookiesStore = cookies(); // Get Cookie on SSR

  const authCookie = await decrypt(cookiesStore.get("login")?.value as string); // Decrypt JWT
  const authInfo: UserCookieResponse = (authCookie as SessionPayload) ?? null; // Convert JWT payload to auth type

  return (
    <html lang="en">
      <head>
        <ColorSchemeScript></ColorSchemeScript>
      </head>
      <body className={inter.className}>
        <AppRouterCacheProvider>
          <ConfigProvider
            theme={{
              components: {},
            }}
          >
            <ReduxProvider authInfo={authInfo}>
              <MantineProvider>
                {authInfo && authInfo?.role !== "GUEST" ? (
                  <div id={styles.dashboard_layout}>
                    <header>
                      <DashboardHeader></DashboardHeader>
                    </header>
                    <main className={styles.main_layout}>{children}</main>
                  </div>
                ) : (
                  <div id={styles.guest_layout}>
                    <header>
                      <Header></Header>
                    </header>
                    <main className={styles.main_layout}>{children}</main>
                    <footer>
                      <Footer></Footer>
                    </footer>
                  </div>
                )}
              </MantineProvider>
            </ReduxProvider>
          </ConfigProvider>
        </AppRouterCacheProvider>
      </body>
    </html>
  );
}
